def filter_model_dict(model, pretrained_dict):
    """
    Purges a model dictionary to read it in the future
    Args:
            model: Model pytorch
            pretrained_dict: A pretrained dictionary from which to extract weights

    Returns:
            A filtered dictionary. Dummy things like parallel module are deleted
    """

    model_dict = model.state_dict()

    # 1. filter out unnecessary keys
    filtered_dict = {}
    for k, v in pretrained_dict.items():
        if f"thermal_model.{k}" in model_dict:
            if v.size() == model_dict[f"thermal_model.{k}"].size():
                filtered_dict[f"thermal_model.{k}"] = v
        if f"depth_model.{k}" in model_dict:
            if v.size() == model_dict[f"depth_model.{k}"].size():
                filtered_dict[f"depth_model.{k}"] = v

        # For generator support
        for modality in ['audio', 'thermal', 'rgb', 'depth']:
            # Yolo Generator
            if f"models.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"models.{modality}.{k}"].size():
                    filtered_dict[f"models.{modality}.{k}"] = v
            # EfficientDet Generator
            if f"model_backbones.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"model_backbones.{modality}.{k}"].size():
                    filtered_dict[f"model_backbones.{modality}.{k}"] = v
            if f"model_necks.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"model_necks.{modality}.{k}"].size():
                    filtered_dict[f"model_necks.{modality}.{k}"] = v

        # For parallel support
        if f"module.{k}" in model_dict:
            if v.size() == model_dict[f"module.{k}"].size():
                filtered_dict[f"module.{k}"] = v
        for modality in ['audio', 'thermal', 'rgb', 'depth']:
            # Yolo Generator
            if f"module.models.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"module.models.{modality}.{k}"].size():
                    filtered_dict[f"module.models.{modality}.{k}"] = v
            # EfficientDet Generator
            if f"module.model_backbones.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"module.model_backbones.{modality}.{k}"].size():
                    filtered_dict[f"module.model_backbones.{modality}.{k}"] = v
            if f"module.model_necks.{modality}.{k}" in model_dict:
                if v.size() == model_dict[f"module.model_necks.{modality}.{k}"].size():
                    filtered_dict[f"module.model_necks.{modality}.{k}"] = v

        if k in model_dict:
            if v.size() == model_dict[k].size():
                filtered_dict[k] = v

    logger.debug(f"ModelDict Update:{len(filtered_dict.keys())}/{len(model_dict.keys())}")

    # 2. overwrite entries in the existing state dict
    model_dict.update(filtered_dict)

    return model_dict

def load_pretrained_weights(model, model_name, load_fc=True):
    """ Loads pretrained weights, and downloads if loading for the first time. """
    state_dict = model_zoo.load_url(url_map[model_name])

    # Filter model dict as needed
    state_dict = filter_model_dict(model, state_dict)

    if load_fc:
        model.load_state_dict(state_dict)
    else:
        state_dict.pop('_fc.weight')
        state_dict.pop('_fc.bias')
        res = model.load_state_dict(state_dict, strict=False)
        assert set(res.missing_keys) == set(
            ['_fc.weight', '_fc.bias']), 'issue loading pretrained weights'
    #print('Loaded pretrained weights for {}'.format(model_name))
